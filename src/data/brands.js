export var getBrand = function (name) {
	return brands.find(function (brand) {
		return brand.name === name
	})
}
export var getBrandNames = function () {
	return brands.map(function (brand) {
		return brand.name
	})
}
export var getBrandTypes = function (brand) {
	var brandObj = getBrand(brand) || {}
	return brandObj.types.map(function (type) {
		return type.name
	})
}
export var getBrandTypeDimensions = function (brand, type) {
	var brandObj = getBrand(brand) || {}
	console.log(brandObj)
	var typeObj = brandObj.types.find(function (t) {
		return t.name === type
	})
	console.log(typeObj)
	return typeObj.dimensions
}
